#include <string.h>

int		ft_strequ(char const *s1, char const *s2, size_t n)
{
	if (*s1 != *s2)
		return (0);
	else if (*s1 == 0 || n == 0)
		return (1);
	else
		return (ft_strequ(s1 + 1, s2 + 1, n - 1));
}

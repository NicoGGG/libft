#include <stdlib.h>

void	ft_memdel(void **ap)
{
	if (ap == NULL)
	{
		free(ap);
		return ;
	}
	while (*ap)
	{
		free(*ap);
		*ap = NULL;
		ap++;
	}
	ap = NULL;
}
